create or replace view wrestling.trending_athletes as
with athlete_match_results as (
  select
    match.id,
    match.match_date,
    match.winner_id as athlete_id,
    true as is_win
  from wrestling.matches match
  union all
  select
    match.id,
    match.match_date,
    match.loser_id as athlete_id,
    false as is_win
  from wrestling.matches match
),
ordered_results as (
  select
    result.athlete_id,
    result.is_win,
    row_number() over (
      partition by result.athlete_id
      order by result.match_date desc, result.id desc
    ) as row_number
  from athlete_match_results result
),
streak_windows as (
  select
    ordered.athlete_id,
    ordered.is_win,
    ordered.row_number,
    min(case when not ordered.is_win then ordered.row_number end) over (
      partition by ordered.athlete_id
    ) as first_loss_row
  from ordered_results ordered
),
win_streaks as (
  select
    streak.athlete_id,
    coalesce(
      sum(
        case
          when streak.is_win
            and streak.row_number < coalesce(streak.first_loss_row, 2147483647)
          then 1
          else 0
        end
      ),
      0
    )::integer as recent_win_streak
  from streak_windows streak
  group by streak.athlete_id
),
profile_views_24h as (
  select
    view_log.athlete_id,
    count(*)::integer as views_last_24h
  from wrestling.profile_views view_log
  where view_log.created_at >= now() - interval '24 hours'
  group by view_log.athlete_id
)
select
  profile.id as athlete_id,
  profile.full_name,
  profile.state,
  profile.gender,
  profile.high_school_name,
  profile.club_name,
  profile.unified_score,
  profile.previous_rank,
  coalesce(streak.recent_win_streak, 0) as recent_win_streak,
  coalesce(view_count.views_last_24h, 0) as profile_views_24h,
  (
    coalesce(streak.recent_win_streak, 0) * 2
    + coalesce(view_count.views_last_24h, 0) * 1
  )::numeric(10, 2) as trend_score
from wrestling.profiles profile
left join win_streaks streak on streak.athlete_id = profile.id
left join profile_views_24h view_count on view_count.athlete_id = profile.id
where coalesce(profile.unified_score, 0) > 0;
grant select on wrestling.trending_athletes to anon, authenticated, service_role;
