create table if not exists wrestling.ranking_sync_status (
  id bigint generated by default as identity primary key,
  source text not null default 'season_recalculation',
  last_synced_at timestamptz not null default now(),
  total_athletes_indexed bigint not null default 0,
  eligible_athletes_indexed bigint not null default 0,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);
create index if not exists ranking_sync_status_last_synced_idx
  on wrestling.ranking_sync_status (last_synced_at desc);
alter table wrestling.ranking_sync_status enable row level security;
grant select on wrestling.ranking_sync_status to anon, authenticated;
grant select, insert, update, delete on wrestling.ranking_sync_status to service_role;
drop policy if exists "Public can read ranking sync status" on wrestling.ranking_sync_status;
create policy "Public can read ranking sync status"
on wrestling.ranking_sync_status
for select
using (true);
create or replace function public.get_latest_ranking_status()
returns table (
  last_updated timestamptz,
  next_update timestamptz,
  total_athletes_indexed bigint,
  eligible_athletes_indexed bigint
)
language sql
stable
as $$
  select
    status.last_synced_at as last_updated,
    status.last_synced_at + interval '7 days' as next_update,
    status.total_athletes_indexed,
    status.eligible_athletes_indexed
  from wrestling.ranking_sync_status status
  order by status.last_synced_at desc
  limit 1;
$$;
grant execute on function public.get_latest_ranking_status() to anon, authenticated, service_role;
create table if not exists wrestling.profile_views (
  id uuid primary key default gen_random_uuid(),
  athlete_id uuid not null references wrestling.profiles(id) on delete cascade,
  viewer_user_id uuid not null references auth.users(id) on delete cascade,
  viewer_role text not null default 'recruiter',
  viewer_state text,
  created_at timestamptz not null default now()
);
create index if not exists profile_views_athlete_created_idx
  on wrestling.profile_views (athlete_id, created_at desc);
create index if not exists profile_views_viewer_athlete_created_idx
  on wrestling.profile_views (viewer_user_id, athlete_id, created_at desc);
alter table wrestling.profile_views enable row level security;
grant select, insert on wrestling.profile_views to authenticated;
grant select, insert, update, delete on wrestling.profile_views to service_role;
drop policy if exists "Recruiters can insert their own profile views" on wrestling.profile_views;
create policy "Recruiters can insert their own profile views"
on wrestling.profile_views
for insert
to authenticated
with check (auth.uid() = viewer_user_id);
drop policy if exists "Users can read relevant profile views" on wrestling.profile_views;
create policy "Users can read relevant profile views"
on wrestling.profile_views
for select
to authenticated
using (
  viewer_user_id = auth.uid()
  or exists (
    select 1
    from wrestling.profiles profile
    where profile.id = athlete_id
      and profile.user_id = auth.uid()
  )
);
